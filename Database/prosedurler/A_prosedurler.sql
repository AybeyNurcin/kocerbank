    SET AD              = P_AD,
        SOYAD           = P_SOYAD,
        EPOSTA          = P_EPOSTA,
        SIFRE           = P_SIFRE,
        DOGUMTARIHI     = P_DOGUMTARIHI,
        TELEFONNO       = P_TELEFONNO,
        TCKN            = P_TCKN,
        CINSIYET        = P_CINSIYET,
        VKN             = P_VKN,
        MUSTERITIPI     = P_MUSTERITIPI,
        SUBESUBEKODU    = P_SUBESUBEKODU,
        DURUMKODU       = P_DURUMKODU,
        UNVAN           = P_UNVAN
    WHERE
        ID = P_ID;
END KB_MUSTERIBILGILERI_GUNCELLE;
