CREATE OR REPLACE PROCEDURE KB_MUSTERIBILGILERI_GETIR
(
    P_ID    IN KB_MUSTERIBILGILERI.ID%TYPE,

    P_SONUC OUT SYS_REFCURSOR
)
AS
BEGIN
    OPEN P_SONUC FOR SELECT * FROM KB_MUSTERIBILGILERI WHERE ID = P_ID;
END KB_MUSTERIBILGILERI_GETIR;
/




CREATE OR REPLACE PROCEDURE KB_MUSTERIBILGILERI_LISTELE
(
    P_AD                    IN KB_MUSTERIBILGILERI.AD%TYPE,
    P_SOYAD                 IN KB_MUSTERIBILGILERI.SOYAD%TYPE,
    P_EPOSTA                IN KB_MUSTERIBILGILERI.EPOSTA%TYPE,
    P_DOGUMTARIHI           IN KB_MUSTERIBILGILERI.DOGUMTARIHI%TYPE,
    P_TELEFONNO             IN KB_MUSTERIBILGILERI.TELEFONNO%TYPE,
    P_TCKN                  IN KB_MUSTERIBILGILERI.TCKN%TYPE,
    P_CINSIYET              IN KB_MUSTERIBILGILERI.CINSIYET%TYPE,
    P_VKN                   IN KB_MUSTERIBILGILERI.VKN%TYPE,
    P_MUSTERITIPI           IN KB_MUSTERIBILGILERI.MUSTERITIPI%TYPE,
    P_SUBESUBEKODU          IN KB_MUSTERIBILGILERI.SUBESUBEKODU%TYPE,
    P_DURUMKODU             IN KB_MUSTERIBILGILERI.DURUMKODU%TYPE,
    P_UNVAN                 IN KB_MUSTERIBILGILERI.UNVAN%TYPE,
    P_KAYITOLUSTURMATARIHI  IN KB_MUSTERIBILGILERI.KAYITOLUSTURMATARIHI%TYPE,

    
    P_SONUC                 OUT SYS_REFCURSOR
)
AS
BEGIN
    OPEN P_SONUC FOR SELECT * FROM KB_MUSTERIBILGILERI
    WHERE
        (P_AD IS NULL OR UPPER(AD) LIKE '%' || UPPER(P_AD) || '%')
    AND (P_SOYAD IS NULL OR UPPER(SOYAD) LIKE '%' || UPPER(P_SOYAD) || '%')
    AND (P_EPOSTA IS NULL OR UPPER(EPOSTA) LIKE '%' || UPPER(P_EPOSTA) || '%')
    AND (P_DOGUMTARIHI IS NULL OR DOGUMTARIHI = P_DOGUMTARIHI)
    AND (P_TELEFONNO IS NULL OR UPPER(TELEFONNO) LIKE '%' || UPPER(P_TELEFONNO) || '%')
    AND (P_TCKN IS NULL OR TCKN = P_TCKN)
    AND (P_CINSIYET IS NULL OR CINSIYET = P_CINSIYET)
    AND (P_VKN IS NULL OR VKN = P_VKN)
    AND (P_MUSTERITIPI IS NULL OR MUSTERITIPI = P_MUSTERITIPI)
    AND (P_SUBESUBEKODU IS NULL OR SUBESUBEKODU = P_SUBESUBEKODU)
    AND (P_DURUMKODU IS NULL OR DURUMKODU = P_DURUMKODU)
    AND (P_UNVAN IS NULL OR UPPER(UNVAN) = UPPER(P_UNVAN))
    AND (P_KAYITOLUSTURMATARIHI IS NULL OR KAYITOLUSTURMATARIHI = P_KAYITOLUSTURMATARIHI);
END KB_MUSTERIBILGILERI_LISTELE;
/





CREATE OR REPLACE PROCEDURE KB_MUSTERIBILGILERI_SIL
(
    P_ID IN KB_MUSTERIBILGILERI.ID%TYPE
)
AS
BEGIN
    DELETE FROM KB_MUSTERIBILGILERI WHERE ID = P_ID;
END KB_MUSTERIBILGILERI_SIL;
