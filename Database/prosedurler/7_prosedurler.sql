/* Personel Ekleme Prosedürü */

CREATE OR REPLACE PROCEDURE KB_PERSONEL_EKLE(
    P_PERSONEL_ADI            IN  KB_PERSONEL.AD%TYPE,
    P_PERSONEL_SOYADI         IN  KB_PERSONEL.SOYAD%TYPE,
    P_PERSONEL_ROLU           IN  KB_PERSONEL.ROL%TYPE,
    P_PERSONEL_SIFRESI        IN  KB_PERSONEL.SIFRE%TYPE,
    P_PERSONEL_TCKN           IN  KB_PERSONEL.TCKN%TYPE,
    P_PERSONEL_TELEFON_NO     IN  KB_PERSONEL.TELEFON_NO%TYPE,
    P_PERSONEL_ADRES          IN  KB_PERSONEL.ADRES%TYPE,
    P_PERSONEL_EPOSTA         IN  KB_PERSONEL.EPOSTA%TYPE,
    P_PERSONEL_SUBEKODU       IN  KB_PERSONEL.SUBESUBEKODU%TYPE,
    P_PERSONEL_DURUMKODU      IN  KB_PERSONEL.DURUMKODU%TYPE,
    P_PERSONEL_RECORDUSER     IN  KB_PERSONEL.RECORDUSER%TYPE,

    P_PERSONEL_ID             OUT KB_PERSONEL.ID%TYPE,
    P_PERSONEL_SICIL          OUT KB_PERSONEL.SICIL%TYPE
)IS
BEGIN
    SELECT KB_PERSONEL_SEQ.NEXTVAL INTO P_PERSONEL_ID FROM DUAL;
    SELECT KB_SICIL_SEQ.NEXTVAL INTO P_PERSONEL_SICIL FROM DUAL;

    INSERT INTO KB_PERSONEL(
        ID,
        AD,
        SOYAD,
        ROL,
        SIFRE,
        TCKN,
        TELEFON_NO,
        ADRES,
        EPOSTA,
        SUBESUBEKODU,
        DURUMKODU,
        RECORDUSER
    ) VALUES (
        P_PERSONEL_ID,
        P_PERSONEL_ADI,
        P_PERSONEL_SOYADI,
        P_PERSONEL_ROLU,
        P_PERSONEL_SIFRESI,
        P_PERSONEL_TCKN,
        P_PERSONEL_TELEFON_NO,
        P_PERSONEL_ADRES,
        P_PERSONEL_EPOSTA,
        P_PERSONEL_SUBEKODU,
        P_PERSONEL_DURUMKODU,
        P_PERSONEL_RECORDUSER
    );
END ;
/

/* ID'ye göre personel bilgilerini getirir */

CREATE OR REPLACE PROCEDURE KB_PERSONEL_READ_BY_ID(
    P_PERSONEL_ID     IN  KB_PERSONEL.ID%TYPE,
    P_CURSOR OUT SYS_REFCURSOR
) IS
BEGIN
    OPEN P_CURSOR FOR
        SELECT * FROM KB_PERSONEL WHERE ID = P_PERSONEL_ID;
END ;
/

/* İstediğim kriterlere göre personel bilgilerini getirir */

CREATE OR REPLACE PROCEDURE KB_PERSONEL_READ_BY_LISTELE(
    P_PERSONEL_ADI        IN  KB_PERSONEL.AD%TYPE,
    P_PERSONEL_SOYADI     IN  KB_PERSONEL.SOYAD%TYPE,
    P_PERSONEL_ROLU       IN  KB_PERSONEL.ROL%TYPE,
    P_PERSONEL_TCKN       IN  KB_PERSONEL.TCKN%TYPE,
    P_PERSONEL_TELEFON_NO IN  KB_PERSONEL.TELEFON_NO%TYPE,
    P_PERSONEL_SUBEKODU   IN  KB_PERSONEL.SUBESUBEKODU%TYPE,
    P_PERSONEL_ADRES      IN  KB_PERSONEL.ADRES%TYPE,
    P_PERSONEL_EPOSTA     IN  KB_PERSONEL.EPOSTA%TYPE,
    P_PERSONEL_DURUMKODU  IN  KB_PERSONEL.DURUMKODU%TYPE,
    P_CURSOR              OUT SYS_REFCURSOR
) IS
BEGIN
    OPEN P_CURSOR FOR
        SELECT * FROM PERSONEL
        WHERE (AD           = P_PERSONEL_ADI IS NULL OR P_PERSONEL_ADI LIKE '%' || P_PERSONEL_ADI || '%')
        AND   (SOYAD        = P_PERSONEL_SOYADI IS NULL OR P_PERSONEL_SOYADI LIKE '%' || P_PERSONEL_SOYADI || '%')
        AND   (ROL          = P_PERSONEL_ROLU OR P_PERSONEL_ROLU IS NULL)
        AND   (TCKN         = P_PERSONEL_TCKN OR P_PERSONEL_TCKN IS NULL)
        AND   (TELEFON_NO   = P_PERSONEL_TELEFON_NO OR P_PERSONEL_TELEFON_NO IS NULL)
        AND   (SUBESUBEKODU = P_PERSONEL_SUBEKODU OR P_PERSONEL_SUBEKODU IS NULL)
        AND   (ADRES        = P_PERSONEL_ADRES IS NULL OR P_PERSONEL_ADRES LIKE '%' || P_PERSONEL_ADRES || '%')
        AND   (EPOSTA       = P_PERSONEL_EPOSTA OR P_PERSONEL_EPOSTA IS NULL)
        AND   (DURUMKODU    = P_PERSONEL_DURUMKODU OR P_PERSONEL_DURUMKODU IS NULL);
END ;
/

/* Personel Güncelleme Prosedürü */

CREATE OR REPLACE PROCEDURE KB_PERSONEL_GUNCELLE(
    P_PERSONEL_ID           IN KB_PERSONEL.ID%TYPE,
    P_PERSONEL_ADI           IN KB_PERSONEL.AD%TYPE,
    P_PERSONEL_SOYADI        IN KB_PERSONEL.SOYAD%TYPE,
    P_PERSONEL_ROLU          IN KB_PERSONEL.ROL%TYPE,
    P_PERSONEL_SIFRESI        IN KB_PERSONEL.SIFRE%TYPE,
    P_PERSONEL_TCKN         IN KB_PERSONEL.TCKN%TYPE,
    P_PERSONEL_TELEFON_NO   IN KB_PERSONEL.TELEFON_NO%TYPE,
    P_PERSONEL_ADRES        IN KB_PERSONEL.ADRES%TYPE,
    P_PERSONEL_EPOSTA       IN KB_PERSONEL.EPOSTA%TYPE,
    P_PERSONEL_SUBEKODU     IN KB_PERSONEL.SUBESUBEKODU%TYPE,
    P_PERSONEL_DURUMKODU    IN KB_PERSONEL.DURUMKODU%TYPE,
    P_PERSONEL_RECORDUSER   IN KB_PERSONEL.RECORDUSER%TYPE
) IS
BEGIN
    UPDATE KB_PERSONEL
    SET AD           = P_PERSONEL_ADI,
        SOYAD        = P_PERSONEL_SOYADI,
        ROL          = P_PERSONEL_ROLU,
        SIFRE        = P_PERSONEL_SIFRESI,
        TCKN         = P_PERSONEL_TCKN,
        TELEFONNO   = P_PERSONEL_TELEFON_NO,
        ADRES        = P_PERSONEL_ADRES,
        EPOSTA       = P_PERSONEL_EPOSTA,
        SUBESUBEKODU = P_PERSONEL_SUBEKODU,
        DURUMKODU    = P_PERSONEL_DURUMKODU,
        RECORDUSER   = P_PERSONEL_RECORDUSER
    WHERE ID = P_PERSONEL_ID;
END ;
/

/* Personel Silme Prosedürü */

CREATE OR REPLACE PROCEDURE KB_PERSONEL_SIL(
    P_PERSONEL_ID     IN KB_PERSONEL.ID%TYPE
) IS
BEGIN
    DELETE FROM KB_PERSONEL WHERE ID = P_PERSONEL_ID;
END ;
